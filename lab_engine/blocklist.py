def is_blocked_name(name: str) -> bool:
    nl = name.lower().strip()
    if nl in NAME_BLOCKLIST_EXACT: return True
    for s in NAME_BLOCKLIST_SUBSTR:
        if s in nl: return True
    return False

def categorize(name: str, group: str = "") -> str:
    nl = name.lower()
    for ab in ANTIBIOTICS_HINT:
        if ab in nl: return "antibiotic"
    # Спец-группы (из спец-парсера) — попадают в main с собственной группой
    if group in ("Спермограмма / TUNEL", "Биоимпеданс"):
        return "main"
    for sp in SPERM_HINT:
        if sp in nl: return "sperm"
    return "main"

def norm_unit(u: str) -> str:
    if not u: return ""
    s = u.strip().lower().replace(" ", "")
    s = s.replace("ммоль/л", "mmol/l").replace("моль/л", "mol/l")
    s = s.replace("мкмоль/л", "umol/l").replace("μмоль/л", "umol/l")
    s = s.replace("ммоль/мл", "mmol/ml")
    s = s.replace("нмоль/л", "nmol/l").replace("пмоль/л", "pmol/l")
    s = s.replace("мг/дл", "mg/dl").replace("мг/л", "mg/l").replace("мг/мл", "mg/ml")
    s = s.replace("мкг/мл", "ug/ml").replace("нг/мл", "ng/ml").replace("нг/л", "ng/l").replace("пг/мл", "pg/ml")
    s = s.replace("г/дл", "g/dl").replace("г/л", "g/l").replace("г/мл", "g/ml")
    s = s.replace("ме/мл", "iu/ml").replace("ме/л", "iu/l").replace("мме/л", "miu/l")
    s = s.replace("мкме/мл", "uiu/ml")
    s = s.replace("ед/л", "u/l")
    s = s.replace("мкг/л", "ug/l").replace("мкг/дл", "ug/dl")
    s = s.replace("μ", "u")
    return s
